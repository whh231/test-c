#include<stdio.h>
#include<math.h>
int main(){
	int n;
	int x;
	scanf("%d",&n);
	if(n<=100000){
		x=n/10;	
	}
	else if(n>100000&&n<=200000){
		x=10000+(n-100000)*0.075;
	}
	else if(n>200000&&n<=400000){
		x=10000+7500+(n-200000)*0.05;
	}
	else if(n>400000&&n<=600000){
		x=10000+7500+(n-400000)*0.03+10000;
	}
	else if(n>600000&&n<=1000000){
		x=10000+7500+(n-600000)*0.015+10000+6000;
	}
	else {
	x=10000+7500+(n-1000000)*0.01+10000+6000+6000;
	}
	printf("%d",x);
	return 0;
}